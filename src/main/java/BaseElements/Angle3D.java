package BaseElements;

/**
 * Angle in 3D Space, contains x, y, z values for rotate object
 */
public class Angle3D {

    float angleX;
    float angleY;
    float angleZ;

    public Angle3D(float angleX, float angleY, float angleZ) {
        this.angleX = angleX;
        this.angleY = angleY;
        this.angleZ = angleZ;
    }
}
